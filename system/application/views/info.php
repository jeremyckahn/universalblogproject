<h2>What is the Universal Blog Project?</h2>
	<p>The Universal Blog Project is a social experiment.  It is a blog that displays all of the posts from its users anonymously in a single feed.</p>

	<h2>Why is this interesting?</h2>
	<p>Typically, when a blog is read, it is analyzed in the context of the person who wrote it.  The Universal Blog Project tries to separate the blog from the blogger, in an effort to keep the reader's focus on the content of the blog itself.</p>
	<h2>Where do I begin?</h2>
	<p>You don't need an account to read the <a href="index.php">blog</a>.  In order to make a post, however, you will need to <a href="scripts/php/signup/signup.php">sign up</a>.  Signing up is simple and free.  Once you have an account, a link to the blog form will appear in the navigation panel to the right.</p>
	<h2>What are the rules for posting?</h2>

	<p>We take a Laissez-faire approach to blog content, but there are a few rules in place.</p>
	<ul>
		<li>
			<h3>No linking.</h3>
			<p>The Universal Blog Project is meant to encourage a focus on blogs.  That focus is lost if readers are being linked every which way.  This also means you may not post pictures.</p>
		</li>
		<li>

			<h3>No HTML/CSS formatting.</h3>
			<p>Social networking sites have taught us something:  Websites work better for the user when the layout is consistent.  For this reason, custom HTML/CSS formatting within posts is not allowed.</p>
		</li>
		<li>
			<h3>Posts have size limits.</h3>
			<p>
				Please keep in mind that your space is limited for blog posts.  Titles are limited at 150 characters, and the blog content is limited to 5000 characters.
			</p>

		</li>
		<li>
			<h3>Use common sense.</h3>
			<p>People other than you are going to be reading your blog posts.  Please do not post anything you would not like to be seen publicly, and try to keep the peace.</p>
		</li>
	</ul>
	<p>There are filters in place to make sure that the content rules are being respected.  Please keep in mind that users have the ability to &quot;blacklist&quot; posts, so if they find your blog post objectionable, they will be able to hide it from their view.</p>

	<h2>Oh, and an important note...</h2>
	<p>The Universal Blog Project is still very much in the early stages of development.  It is not yet feature-complete, and there may be some bugs or other issues.  If you find a bug, please do not hesitate to <a href="mailto:jeremyckahn@gmail.com">email the webmaster</a>.  Your help is greatly appreciated!</p>
